
public class do_while_loop {
	public static void main(String[] args) {
		
		int a = 0;
		while (a<=-1) {//while(a<=-1)-->check
			System.out.println(a);
			a++;
		}
			int b = 0;
			System.out.println("---------------");

			
			do {
				System.out.println(b);
				b++;
			
			}while (b<=-1); //while(b<=-1)-->check
	}


}

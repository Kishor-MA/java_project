
public class logical_operators {
	public static void main(String[] args) {
		//&& -> AND   || -> OR
		
		int subject1 = 40;
		int subject2 = 60;
		
		if ((subject1 >= 45) && (subject2 >= 35)) {
			System.out.println("Yes it is true");

		}
		else {
			System.out.println("No it is false");

		}
		
		int subject3 = 20;
		int subject4 = 30;
				
		
		if ((subject3 >= 45) || (subject4 >= 35)) {
			System.out.println("Yes it is true");

		}
		else {
			System.out.println("No it is false");

		}
		

	}


}
